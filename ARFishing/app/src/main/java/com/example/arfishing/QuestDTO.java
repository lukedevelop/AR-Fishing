public class QuestDTO {

    String questName;
    int questCount;
    int questNow;
    int questComplete;

    public QuestDTO(String questName, int questCount, int questNow, int questComplete) {
        this.questName = questName;
        this.questCount = questCount;
        this.questNow = questNow;
        this.questComplete = questComplete;

    }




}
